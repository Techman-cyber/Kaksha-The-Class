function showPoetryQuizQuestion() {
  if (currentPoetryQuizIndex >= poetryQuizQuestions.length) {
    document.getElementById('poetryQuiz').innerHTML = '<p class="quiz-feedback" data-i18n="quiz.complete">बधाई! आपने सभी प्रश्नों का अभ्यास कर लिया।</p>';
    document.getElementById('nextPoetryQuizBtn').style.display = 'none';
    return;
  }
  const q = poetryQuizQuestions[currentPoetryQuizIndex];
  let html = `<div class="quiz-question">${q.question}</div>`;
  html += '<div class="quiz-options">';
  q.options.forEach((opt, idx) => {
    html += `<div class="quiz-option" onclick="selectPoetryQuizOption(${idx})">${opt}</div>`;
  });
  html += '</div>';
  document.getElementById('poetryQuiz').innerHTML = html;
  document.getElementById('poetryQuizFeedback').innerHTML = '';
  document.getElementById('nextPoetryQuizBtn').style.display = 'none';
}

function selectPoetryQuizOption(selectedIdx) {
  const q = poetryQuizQuestions[currentPoetryQuizIndex];
  const options = document.querySelectorAll('#poetryQuiz .quiz-option');
  options.forEach((opt, idx) => {
    opt.classList.remove('correct', 'wrong');
    if (idx === q.answer) opt.classList.add('correct');
    if (idx === selectedIdx && idx !== q.answer) opt.classList.add('wrong');
  });
  poetryUserAnswers[currentPoetryQuizIndex] = selectedIdx;
  document.getElementById('poetryQuizFeedback').innerHTML = selectedIdx === q.answer ? 
    '<span style="color:green;">✔ सही उत्तर!</span>' : 
    `<span style="color:red;">✘ गलत! सही उत्तर है: ${q.options[q.answer]}</span>`;
  document.getElementById('nextPoetryQuizBtn').style.display = 'inline-block';
}

function nextPoetryQuizQuestion() {
  currentPoetryQuizIndex++;
  showPoetryQuizQuestion();
}

