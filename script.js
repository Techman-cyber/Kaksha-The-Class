function showQuizQuestion() {
  if (currentQuizIndex >= quizQuestions.length) {
    document.getElementById('quiz').innerHTML = '<p class="quiz-feedback" data-i18n="quiz.complete">बधाई! आपने सभी प्रश्नों का अभ्यास कर लिया।</p>';
    document.getElementById('nextQuizBtn').style.display = 'none';
    return;
  }
  const q = quizQuestions[currentQuizIndex];
  let html = `<div class="quiz-question">${q.question}</div>`;
  html += '<div class="quiz-options">';
  q.options.forEach((opt, idx) => {
    html += `<div class="quiz-option" onclick="selectQuizOption(${idx})">${opt}</div>`;
  });
  html += '</div>';
  document.getElementById('quiz').innerHTML = html;
  document.getElementById('quizFeedback').innerHTML = '';
  document.getElementById('nextQuizBtn').style.display = 'none';
}

function selectQuizOption(selectedIdx) {
  const q = quizQuestions[currentQuizIndex];
  const options = document.querySelectorAll('#quiz .quiz-option');
  options.forEach((opt, idx) => {
    opt.classList.remove('correct', 'wrong');
    if (idx === q.answer) opt.classList.add('correct');
    if (idx === selectedIdx && idx !== q.answer) opt.classList.add('wrong');
  });
  userAnswers[currentQuizIndex] = selectedIdx;
  document.getElementById('quizFeedback').innerHTML = selectedIdx === q.answer ? 
    '<span style="color:green;">✔ सही उत्तर!</span>' : 
    `<span style="color:red;">✘ गलत! सही उत्तर है: ${q.options[q.answer]}</span>`;
  document.getElementById('nextQuizBtn').style.display = 'inline-block';
}

function nextQuizQuestion() {
  currentQuizIndex++;
  showQuizQuestion();
}

