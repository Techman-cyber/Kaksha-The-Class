function renderQA() {
  const container = document.getElementById('qaAccordion');
  if (!container || container.children.length > 0) return;
  const sections = [
    { title: 'संक्षिप्त प्रश्न', data: qaData.veryShort },
    { title: '25-30 शब्दों में', data: qaData.short25_30 },
    { title: '50-60 शब्दों में', data: qaData.long50_60 },
    { title: 'आशय स्पष्ट', data: qaData.aashay }
  ];
  sections.forEach((sec) => {
    const sectionDiv = document.createElement('div');
    sectionDiv.className = 'accordion-section';
    const header = document.createElement('div');
    header.className = 'section-header';
    header.innerHTML = `${sec.title} <i class="fas fa-chevron-down"></i>`;
    header.addEventListener('click', () => {
      header.classList.toggle('active');
      content.classList.toggle('show');
    });
    const content = document.createElement('div');
    content.className = 'section-content';
    sec.data.forEach(item => {
      const itemDiv = document.createElement('div');
      itemDiv.className = 'qa-item';
      itemDiv.innerHTML = `<div class="qa-question">${item.q}</div><div class="qa-answer">${item.a}</div>`;
      content.appendChild(itemDiv);
    });
    sectionDiv.appendChild(header);
    sectionDiv.appendChild(content);
    container.appendChild(sectionDiv);
  });
}

