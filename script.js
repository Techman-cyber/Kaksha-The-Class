function startTest(source) {
  // Clear previous timer if any
  if (timerInterval) clearInterval(timerInterval);
  timeLeft = 5 * 60; // 5 minutes
  updateTimerDisplay();

  // Select 10 random questions from the appropriate test bank
  if (source === 'story') {
    currentTestQuestions = shuffleArray(storyTestBank).slice(0, 10);
  } else if (source === 'poetry') {
    currentTestQuestions = shuffleArray(poetryTestBank).slice(0, 10);
  }

  renderTestMCQ();
  startTimer();
}

function shuffleArray(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function renderTestMCQ() {
  let html = '<div class="test-question-block"><h3>वस्तुनिष्ठ प्रश्न (10)</h3>';
  currentTestQuestions.forEach((q, index) => {
    html += `<div class="test-question">${index+1}. ${q.question}</div>`;
    html += '<div class="test-options">';
    q.options.forEach((opt, optIdx) => {
      html += `<div class="test-option"><input type="radio" name="q_${index}" value="${optIdx}"> ${opt}</div>`;
    });
    html += '</div>';
  });
  html += '</div>';

  document.getElementById('testContent').innerHTML = html;
  document.getElementById('testResults').style.display = 'none';
  document.getElementById('submitTest').style.display = 'inline-block';
}

function startTimer() {
  timerInterval = setInterval(() => {
    timeLeft--;
    updateTimerDisplay();
    if (timeLeft <= 0) {
      clearInterval(timerInterval);
      submitTest(); // auto-submit when time runs out
    }
  }, 1000);
}

function updateTimerDisplay() {
  const minutes = Math.floor(timeLeft / 60);
  const seconds = timeLeft % 60;
  document.getElementById('timer').innerText = 
    `${minutes.toString().padStart(2,'0')}:${seconds.toString().padStart(2,'0')}`;
}

function submitTest() {
  clearInterval(timerInterval);
  
  let resultsHtml = '<h3>परिणाम</h3>';
  let score = 0;

  resultsHtml += '<h4>वस्तुनिष्ठ प्रश्न</h4>';
  currentTestQuestions.forEach((q, index) => {
    const selected = document.querySelector(`input[name="q_${index}"]:checked`);
    const userAnswer = selected ? parseInt(selected.value) : -1;
    const isCorrect = (userAnswer === q.answer);
    if (isCorrect) score++;
    
    resultsHtml += `<div class="result-item">`;
    resultsHtml += `<div class="result-question">${index+1}. ${q.question}</div>`;
    resultsHtml += `<div class="result-answer">आपका उत्तर: ${userAnswer !== -1 ? q.options[userAnswer] : 'कोई नहीं'} ${isCorrect ? '✅' : '❌'}</div>`;
    resultsHtml += `<div>सही उत्तर: ${q.options[q.answer]}</div>`;
    resultsHtml += `</div>`;
  });

  resultsHtml += `<p style="font-size:1.5rem; margin-top:2rem;">आपका स्कोर: ${score} / 10</p>`;
  resultsHtml += `<p style="font-size:1.2rem;">प्रतिशत: ${(score/10*100).toFixed(1)}%</p>`;

  document.getElementById('testResults').innerHTML = resultsHtml;
  document.getElementById('testResults').style.display = 'block';
  document.getElementById('submitTest').style.display = 'none';
}

