// ---------- NOTIFICATION LOGIC ----------
function setupNotifications() {
  document.querySelectorAll('.notification').forEach(notif => {
    const closeBtn = notif.querySelector('.close-notification');
    const actionBtn = notif.querySelector('.notif-action');
    if (closeBtn) {
      closeBtn.addEventListener('click', function() { notif.remove(); });
    }
    if (actionBtn) {
      actionBtn.addEventListener('click', function() {
        const url = actionBtn.getAttribute('data-url');
        if (url) window.open(url, '_blank');
        notif.remove();
      });
    }
  });
}
setTimeout(() => {
  const ytNotif = document.getElementById('subscribeNotification');
  if (ytNotif) ytNotif.classList.add('show');
}, 4000);
setTimeout(() => {
  const instaNotif = document.getElementById('instaNotification');
  if (instaNotif) instaNotif.classList.add('show');
}, 6000);
setupNotifications();

// ---------- VIEW NAVIGATION ----------
function showView(viewId) {
  document.querySelectorAll('.view').forEach(v => v.classList.remove('active'));
  var target = document.getElementById(viewId);
  if (target) target.classList.add('active');
}

// ---------- CLASS SELECTOR ----------
function initClassSelector() {
  const container = document.getElementById('classSelector');
  for (let i = 1; i <= 12; i++) {
    const btn = document.createElement('button');
    btn.className = 'class-btn';
    btn.textContent = i;
    btn.setAttribute('data-class', i);
    btn.onclick = () => selectClass(i);
    container.appendChild(btn);
  }
}
initClassSelector();

function selectClass(cls) {
  selectedClass = cls;
  document.querySelectorAll('.class-btn').forEach(btn => {
    btn.classList.toggle('active-class', btn.getAttribute('data-class') == cls);
  });
  document.getElementById('syllabusCards').style.display = 'grid';
}

